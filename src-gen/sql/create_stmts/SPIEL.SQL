CREATE TABLE IF NOT EXISTS HUB_SPIEL (
    GAMEID VARCHAR(10),
   PRIMARY KEY( GAMEID)
)
ENGINE=InnoDB
DEFAULT CHARSET=utf8
COLLATE=utf8_general_ci;

