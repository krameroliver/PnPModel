DROP TABLE IF EXISTS SAT_SPIEL;


CREATE TABLE IF NOT EXISTS SAT_SPIEL (
GAMENAME VARCHAR(255),
GAMEID VARCHAR(10),
PRIMARY KEY( GAMEID),
CHARACKTERID DECIMAL(10, 2),
GAMETYPE VARCHAR(255)
)
ENGINE=InnoDB
DEFAULT CHARSET=utf8
COLLATE=utf8_general_ci;

